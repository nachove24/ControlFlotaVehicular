    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String patente = txtPatente.getText();
        String anoChasis = txtAno.getText();
        String cargaMaxChasis = txtCargaMax.getText();
        String colorChasis = txtColor.getText();
        String marcaChasis = txtMarca.getText();
        String modeloChasis = txtModelo.getText();
        String numSerieChasis = txtNumSerie.getText();
        
        
        try {
            control.modificarCha(cha,pat,patente,anoChasis,cargaMaxChasis,colorChasis,marcaChasis,modeloChasis,numSerieChasis);
        } catch (Exception ex) {
            Logger.getLogger(ModificarCha.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        JOptionPane optionPane = new JOptionPane("Se guardo correctamente.");
        optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        JDialog dialog = optionPane.createDialog("Guardado Exitoso");
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
        
        /*MenuAcoplado pantalla = new MenuAcoplado(); 
        pantalla.setVisible(true); 
        pantalla.setLocationRelativeTo(null);*/
        //cerrar ventana
        this.dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

