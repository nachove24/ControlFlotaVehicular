    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String anoChasis = txtAno.getText();
        String cargaMaxChasis = txtCargaMax.getText();
        String colorChasis = txtColor.getText();
        String marcaChasis = txtMarca.getText();
        String modeloChasis = txtModelo.getText();
        String numSerieChasis = txtNumSerie.getText();
        String patenteChasis = txtPatente.getText();

        control.crearCha(anoChasis, cargaMaxChasis, colorChasis, marcaChasis, modeloChasis, numSerieChasis, patenteChasis);

        JOptionPane optionPane = new JOptionPane("Se guardó correctamente.");
        optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        JDialog dialog = optionPane.createDialog("Guardado Exitoso");
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed
    
    
   

